<template>
    <p-button-modal :header-title="$t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_TITLE')"
                    centered
                    size="md"
                    fade
                    backdrop
                    scrollable
                    :loading="loading"
                    :footer-cancel-button-bind="{
                        styleType: 'gray900',
                        outline: true,
                    }"
                    :footer-confirm-button-bind="confirmBtnStyle"
                    :visible.sync="proxyVisible"
                    @confirm="onClickCollectConfirm"
    >
        <template #body>
            <div class="w-full">
                <div class="flex w-full">
                    <p-lazy-img :src="image" :loading="!image"
                                width="5.5rem" height="5.5rem"
                                class="mr-10 flex-grow-0"
                    />
                    <div>
                        <p class="name">
                            {{ collector ? collector.name : '' }}
                        </p>
                        <p class="info">
                            {{ $t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_LABEL_ID') }}: {{ collectorId }}
                        </p>
                        <p class="info">
                            {{ $t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_LABEL_VERSION') }}: {{ version }}
                        </p>
                    </div>
                </div>
                <p class="desc">
                    {{ description }}
                </p>
                <p class="sub-header">
                    {{ $t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_LABEL_OPTION') }}
                </p>
                <p-field-group :label="$t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_CREDENTIALS_LABEL')">
                    <p-text-input :value="credential ? credential.name : $t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_CREDENTIALS_ALL')"
                                  disabled
                                  class="w-full"
                    />
                </p-field-group>
                <p-field-group :label="$t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_ACTION_LABEL')" class="mb-20">
                    <p-select-dropdown v-model="selectedCollectMode" :items="collectModeMenu" />
                </p-field-group>
            </div>
        </template>
    </p-button-modal>
</template>

<script lang="ts">
import {
    toRefs, reactive, computed, SetupContext, watch, UnwrapRef, getCurrentInstance, ComponentRenderProxy,
} from '@vue/composition-api';
import { get } from 'lodash';
import { makeProxy } from '@/lib/compostion-util';

import PButtonModal from '@/components/organisms/modals/button-modal/PButtonModal.vue';
import PFieldGroup from '@/components/molecules/forms/field-group/PFieldGroup.vue';
import PTextInput from '@/components/atoms/inputs/PTextInput.vue';
import PSelectDropdown from '@/components/organisms/dropdown/select-dropdown/PSelectDropdown.vue';
import PLazyImg from '@/components/organisms/lazy-img/PLazyImg.vue';
import { MenuItem } from '@/components/organisms/context-menu/type';
import { showErrorMessage, showSuccessMessage } from '@/lib/util';
import { formValidation, requiredValidation } from '@/components/util/composition-helpers';
import { SpaceConnector } from '@/lib/space-connector';
import { TimeStamp } from '@/models';

interface SecretModel {
    secret_id: string;
    name: string;
    secret_type: 'CREDENTIALS'|'CONFIG'|string;
    'secret_groups': string[];
    'schema': string ;
    'provider': string;
    'service_account_id': string;
    'project_id': string;
    'domain_id': string;
    created_at: TimeStamp;
    tags: object;
}

enum COLLECT_MODE {
    all = 'ALL',
    create = 'CREATE',
    update = 'UPDATE'
}

enum COLLECTOR_STATE {
    enabled = 'ENABLED',
    disabled = 'DISABLED'
}

interface PluginOptions {
    supported_resource_type: string[];
    filter_format: FilterFormat[];
    [key: string]: any;
}

interface CollectorPluginModel {
    plugin_id: string;
    version: string;
    options: PluginOptions;
    secret_id?: string;
    secret_group_id?: string;
    provider?: string;
}

interface FilterFormat {
    name: string;
    type: string;
    change_key: string[];
    resource_type: string;
    object_key?: string;
}

interface CollectorModel {
    collector_id: string;
    name: string;
    state: COLLECTOR_STATE;
    provider: string;
    capability: object;
    plugin_info: CollectorPluginModel;
    priority: number;
    created_at: TimeStamp;
    last_collected_at: TimeStamp | null;
    tags: object;
}


interface Props {
    visible: boolean;
    credentialId: string | null;
    collectorId: string;
}

const map = {
    key: 'key',
    label: 'name',
    required: 'is_required',
    placeholder: 'example',
    type: 'type',
    default: 'default',
    menu: 'enums',
};

const setValidation = (forms, values) => {
    const formKey = map.key;
    const vd = {};

    forms.forEach((form) => {
        vd[form[formKey]] = form[map.required] ? [requiredValidation()] : [];
    });

    const {
        allValidation,
        fieldValidation,
        invalidMsg,
        invalidState,
        isAllValid,
    } = formValidation(values, vd);

    return {
        formKey,
        allValidation,
        fieldValidation,
        invalidMsg,
        invalidState,
        isAllValid,
    };
};

export default {
    name: 'CollectDataModal',
    components: {
        PButtonModal,
        PFieldGroup,
        PTextInput,
        PSelectDropdown,
        PLazyImg,
    },
    props: {
        /* sync */
        visible: Boolean,
        credentialId: {
            type: String,
            default: null,
        },
        collectorId: {
            type: String,
            default: '',
        },
    },
    setup(props: Props, context: SetupContext) {
        const vm = getCurrentInstance() as ComponentRenderProxy;

        const state = reactive({
            loading: false,
            proxyVisible: makeProxy('visible', props, context.emit),
            collector: null as CollectorModel | null,
            credential: null as SecretModel | null,
            showValidation: false,
            collectModeMenu: computed<MenuItem[]>(() => [
                { name: COLLECT_MODE.all, label: vm.$t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_ACTION_GET_ALL'), type: 'item' },
                { name: COLLECT_MODE.create, label: vm.$t('PLUGIN.COLLECTOR.MAIN.COLLECT_MODAL_ACTION_GET_NEW'), type: 'item' },
            ]),
            selectedCollectMode: COLLECT_MODE.all as COLLECT_MODE,
            image: computed<string>(() => get(state.collector, 'tags.icon', '')),
            version: computed<string>(() => get(state.collector, 'plugin_info.version', '')),
            description: computed<string>(() => get(state.collector, 'tags.description', '')),
            filterFormats: computed<any[]>(() => get(state.collector, 'plugin_info.options.filter_format', [])),
            filters: {},
            confirmBtnStyle: computed(() => ({
                styleType: state.loading ? 'gray200' : 'primary-dark',
            })),
        });

        let vdApi = setValidation(state.filterFormats, state.filters);

        const onChange = async (val): Promise<void> => {
            if (!state.showValidation) return;
            await vdApi.fieldValidation(val);
            context.emit('changeValidState', vdApi.isAllValid);
        };

        const onClickReset = (): void => {
            if (state.loading) return;

            state.selectedCollectMode = COLLECT_MODE.all;
            state.showValidation = false;
            state.filters = {};
            vdApi = setValidation(state.filterFormats, state.filters);
        };


        const getCollectParams = () => {
            const params: any = {
                collector_id: props.collectorId,
                collect_mode: state.selectedCollectMode,
            };
            if (props.credentialId) params.secret_id = props.credentialId;
            return params;
        };

        const collectorApi = SpaceConnector.client.inventory.collector.collect;
        const onClickCollectConfirm = async (): Promise<void> => {
            state.loading = true;
            state.showValidation = true;
            try {
                await collectorApi(getCollectParams());
                showSuccessMessage(vm.$t('PLUGIN.COLLECTOR.MAIN.ALT_S_COLLECT_START_TITLE'), '', vm.$root);
            } catch (e) {
                console.error(e);
                showErrorMessage(vm.$t('PLUGIN.COLLECTOR.MAIN.ALT_E_COLLECT_TITLE'), e, vm.$root);
            } finally {
                state.proxyVisible = false;
            }
            // }
            state.loading = false;
        };


        const getCollector = async (): Promise<void> => {
            state.loading = true;
            try {
                const res = await SpaceConnector.client.inventory.collector.get({
                    collector_id: props.collectorId,
                });
                state.collector = res;
            } catch (e) {
                console.error(e);
            } finally {
                state.loading = false;
            }
        };

        const secretManagerApi = SpaceConnector.client.secret.secret.get;
        const getCredential = async (): Promise<void> => {
            state.loading = true;
            state.credential = null;
            try {
                const res = await secretManagerApi({
                    secret_id: props.credentialId,
                });
                state.credential = res;
            } catch (e) {
                console.error(e);
            } finally {
                state.loading = false;
            }
        };

        watch(() => props.collectorId, (id) => {
            if (id) getCollector();
        }, { immediate: true });

        watch(() => props.credentialId, (id) => {
            if (id) getCredential();
            else state.credential = null;
        }, { immediate: true });

        return {
            ...toRefs(state),
            vdApi,
            onChange,
            onClickReset,
            onClickCollectConfirm,
        };
    },
};
</script>

